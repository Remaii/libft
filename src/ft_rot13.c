#include "libft.h"

void	ft_rot13(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 97 && str[i] <= 109) || (str[i] >= 65 && str[i] <= 77))
			ft_putchar(str[i] + 13);
		else if ((str[i] > 109 && str[i] <= 122) ||
				(str[i] > 77 && str[i] <= 90))
			ft_putchar(str[i] - 13);
		else
			ft_putchar(str[i]);
		i++;
	}
}
